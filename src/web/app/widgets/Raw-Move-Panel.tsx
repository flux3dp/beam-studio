    handleMoveEnd = () => {
        const {onMoveEnd} = this.props;
        const {currentX, currentY} = this.state;
        if (onMoveEnd) {
            onMoveEnd(currentX, currentY);
        }
    }

    estimateMoveTime = (x, y) => {
        const {currentX, currentY, feedrate} = this.state;
        const dist = Math.hypot(currentX - x, currentY - y); //mm
        const estimatedTime = dist / feedrate * 60; //s
        return new Promise((resolve) => {
            setTimeout(() => {
                resolve(null);
            }, estimatedTime * 1000);
        })
    }

    rawHome = async () => {
        const {onMoveStart} = this.props;
        if (onMoveStart) {
            onMoveStart();
        }
        await DeviceMaster.rawHome();
        this.setState({
            currentX: 0,
            currentY: 0
        }, () => this.handleMoveEnd());
    }


    rawMove = async (x, y) => {
        const {feedrate, workarea, safeDistance, isMoving} = this.state;
        if (isMoving) {
            return;
        }
        this.setState({isMoving: true});
        const {onMoveStart} = this.props;
        if (onMoveStart) {
            onMoveStart();
        }
        x = Math.max(0, Math.min(workarea.maxX - safeDistance.x, x));
        y = Math.max(0, Math.min(workarea.maxY - safeDistance.y, y));
        await DeviceMaster.rawMove({
            x,
            y,
            f: feedrate, // mm / min
        });
        await this.estimateMoveTime(x, y);
        this.setState({
            currentX: x,
            currentY: y,
            isMoving: false
        }, () => this.handleMoveEnd());
    }

    rawMoveRelative = async (x=0, y=0) => {
        const {currentX, currentY} = this.state;
        this.rawMove(currentX + x, currentY + y);
    }

